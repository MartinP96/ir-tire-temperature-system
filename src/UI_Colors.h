#ifndef _UI_COLORS_H_
#define _UI_COLORS_H_

// Assign human-readable names to some common 16-bit color values:
#define  BLACK   0x0000
#define BLUE    0x001F
#define RED     0xF800
#define GREEN   0x07E0
#define CYAN    0x07FF
#define MAGENTA 0xF81F
#define YELLOW  0xFFE0
#define WHITE   0xFFFF

#define BACKGROUND_COLOR BLACK
#define TEXT_COLOR YELLOW

#endif
